@app.route('/api/config/test-connection', methods=['POST'])
def test_connection():
    """测试 LLM Profile 连通性"""
    data = request.json
    if not data:
        return jsonify({"error": "缺少 profile 参数"}), 400
    
    base_url = data.get('base_url', '')
    api_key = data.get('api_key', '')
    model = data.get('model', '')
    vendor = data.get('vendor', '')
    
    logger.info(f"[CONFIG] 测试连通性 | vendor={vendor} | model={model} | base_url={base_url}")
    
    if not base_url or not api_key:
        return jsonify({"error": "缺少 base_url 或 api_key"}), 400
    
    headers = {
        "Content-Type": "application/json",
        "Authorization": f"Bearer {api_key}"
    }
    
    # 发送一个最小化请求测试连通性
    payload = {
        "model": model,
        "messages": [{"role": "user", "content": "Hi"}],
        "max_tokens": 1
    }
    
    try:
        test_profile = {"base_url": base_url, "vendor": vendor}
        resp = requests.post(
            get_chat_completions_url(test_profile),
            headers=headers,
            json=payload,
            timeout=15
        )
        if resp.status_code == 200:
            result = resp.json()
            model_used = result.get('model', model)
            logger.info(f"[CONFIG] 连通测试成功 | vendor={vendor} | model={model_used}")
            return jsonify({
                "success": True,
                "model": model_used,
                "message": f"连接成功，模型: {model_used}"
            })
        elif resp.status_code == 401 or resp.status_code == 403:
            logger.warning(f"[CONFIG] 连通测试认证失败 | vendor={vendor} | model={model} | status={resp.status_code}")
            return jsonify({"success": False, "error": f"认证失败 ({resp.status_code})"}), 200
        else:
            body = resp.text[:200]
            logger.warning(f"[CONFIG] 连通测试失败 | vendor={vendor} | model={model} | status={resp.status_code} | body={body}")
            return jsonify({"success": False, "error": f"HTTP {resp.status_code}: {body}"}), 200
    except requests.exceptions.Timeout:
        logger.warning(f"[CONFIG] 连通测试超时 | vendor={vendor} | model={model}")
        return jsonify({"success": False, "error": "连接超时 (15s)"}), 200
    except requests.exceptions.ConnectionError:
        logger.warning(f"[CONFIG] 连通测试连接失败 | vendor={vendor} | model={model} | base_url={base_url}")
        return jsonify({"success": False, "error": "无法连接到服务器"}), 200
    except Exception as e:
        logger.error(f"[CONFIG] 连通测试异常 | vendor={vendor} | model={model} | error={str(e)}")
        return jsonify({"success": False, "error": str(e)}), 200


